DECLARE @personID INT = 999;
DECLARE @title VARCHAR(10);

SELECT @title = Title FROM Person.Person
WHERE BusinessEntityID = @personID;

IF @title = 'Mr.'
	PRINT 'HELLO SIR!';
ELSE IF @title = 'Ms.'
	PRINT 'HELLO MA`AM';
ELSE IF @title IS NULL
	PRINT 'NO TITLE FPUND FOR THIS PERSON';

